    private void btLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLogarActionPerformed
        // TODO add your handling code here:

        try {
            String cpf = inputCPF.getText();
            char[] senha = inputSenha.getPassword();
            String senhaConvertida = String.valueOf(senha);
            Connection conn = Conexao.getConexao();
            String sql = "select * from user where cpf = ? and senha = ?";

            PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.setString(1, cpf);
            pstm.setString(2, senhaConvertida);

            ResultSet rs = pstm.executeQuery();

            if (rs.next()){
                int idUsuario = rs.getInt("id_usuario");
                SessaoUsuario.idUsuarioLogado = idUsuario;
                new TelaInicialPosLogin().setVisible(true);
                this.dispose();

                rs.close();
                pstm.close();
                conn.close();

            } else {
                showMessageDialog(this, "Erro! Algum dos campos est√° incorreto!");
            }


        } catch (SQLException e) {
            e.printStackTrace();
            showMessageDialog(this, "Erro ao inserir " + e.getMessage());
        }
    }//GEN-LAST:event_btLogarActionPerformed

